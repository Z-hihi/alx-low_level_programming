#include "3-calc.h"
/**
 * op_add - add two int
 * @a: first int
 * @b: second int
 * Return: sum
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtract two int
 * @a: first int
 * @b: second int
 * Return: difference
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiply two int
 * @a: first int
 * @b: second int
 * Return: result
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - division two int
 * @a: first int
 * @b: second int
 * Return: result
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - modulo two int
 * @a: first int
 * @b: second int
 * Return: result
 */
int op_mod(int a, int b)
{
	return (a % b);
}
